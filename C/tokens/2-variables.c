// A variable in C programming is a named memory location used to store data that can be changed during program execution.

// syntax:

// data_type variable_name = value;

// int age = 29;
// float weight = 67.78;
// char ch1 = 'A';
// char ch2 = 'a';
// char ch3 = '7';

// age → variable storing an integer
// salary → variable storing a decimal number
// grade → variable storing a character


// Rules for Naming Variables

//     - Variable names follow the same rules as identifiers:

//     - Must start with letter or underscore

//     - Cannot start with number

//     - Cannot use keywords

//     - No spaces

//     - Only letters, digits, and underscore

